package mymath

import "math"

func Sqrt(x float64) float64 {
	return math.Sqrt(x)
}

func Pow(x, y float64) float64 {
	return math.Pow(x, y)
}

func Sin(x float64) float64 {
	return math.Sin(x)
}

func Cos(x float64) float64 {
	return math.Cos(x)
}

func Tan(x float64) float64 {
	return math.Tan(x)
}

func Max(x, y float64) float64 {
	return math.Max(x, y)
}

func Min(x, y float64) float64 {
	return math.Min(x, y)
}
